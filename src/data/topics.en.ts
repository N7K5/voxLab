type TopicSeed = readonly [category: string, prompt: string];

function buildTopics(difficulty: Difficulty, seeds: readonly TopicSeed[]): Topic[] {
  return seeds.map(([category, prompt], index) => ({
    id: `${difficulty}-${String(index + 1).padStart(2, '0')}`,
    difficulty,
    category,
    prompt,
  }));
}

