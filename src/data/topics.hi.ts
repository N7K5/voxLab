type HindiTopicSeed = readonly [category: string, prompt: string];

function buildHindiTopics(difficulty: Difficulty, seeds: readonly HindiTopicSeed[]): Topic[] {
  return seeds.map(([category, prompt], index) => ({
    id: `hi-${difficulty}-${String(index + 1).padStart(2, '0')}`,
    language: 'hi',
    difficulty,
    category,
    prompt,
  }));
}

